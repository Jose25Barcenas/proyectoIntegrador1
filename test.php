<p style="color: rgb(240, 83, 10);font-size: 24px;  width: 1150px; text-align: right;"> 7.¿Cuánto tiempo dura la enfermedad?</p>
<p style="color:white; font-size: 24px; width:420px; height: 90px; margin-left:800px;">Los síntomas de dengue generalmente duran entre 2 y 7 días. La mayoría de las personas se recupera en alrededor de una semana.</p>
<p style="color: rgb(240, 83, 10);font-size: 22px;  width: 1150px; text-align: right;"> 8.¿Cómo saber si ya he padecido Dengue?</p>
<p style="color:white; font-size: 22px; width:420px; height: 80px; margin-left:800px;">Si una prueba de NAT o de NS1 tiene un resultado positivo de dengue, se confirma un diagnóstico de dengue actual. Si el resultado de NAT es negativo y el de la prueba de anticuerpos IgM es positivo, el diagnóstico de laboratorio es de infección presunta por dengue.</p>
