<p style="color:rgb(233, 84, 15);font-size: 24px; width:1200px; text-align: right;"> 6.¿Se puede contagiar de persona a persona?</p>
<p style="color:white; font-size: 21px; width:450px; height: 90px; margin-left:790px;">El contagio sólo se produce por la picadura de los mosquitos infectados, nunca de una persona a otra, ni a través de objetos o de la leche materna. Sin embargo, aunque es poco común las mujeres embarazadas pueden contagiar a sus bebés durante el embarazo. El dengue es grave cuando se producen hemorragias. 
</p>

